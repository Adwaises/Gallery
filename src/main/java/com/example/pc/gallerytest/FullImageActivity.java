public class FullImageActivity extends AppCompatActivity {
    int positionImage;
    Intent intent;
    GlobalVariables global;
    int PAGE_COUNT = 10;
    ViewPager pager;
    PagerAdapter pagerAdapter;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setRequestedOrientation(ActivityInfo.SCREEN_ORIENTATION_PORTRAIT);
        setContentView(R.layout.pager);

        global=((GlobalVariables)getApplicationContext());
        intent = getIntent();
        positionImage = intent.getExtras().getInt("id");
        PAGE_COUNT = global.getMediaList().size();
        pager = (ViewPager) findViewById(R.id.pager);
        pagerAdapter = new MyFragmentPagerAdapter(getSupportFragmentManager());
        pager.setAdapter(pagerAdapter);
        pager.setCurrentItem(positionImage);

        pager.setOnPageChangeListener(new ViewPager.OnPageChangeListener() {

            @Override
            public void onPageSelected(int position) {
            }

            @Override
            public void onPageScrolled(int position, float positionOffset,
                                       int positionOffsetPixels) {
            }

            @Override
            public void onPageScrollStateChanged(int state) {
            }
        });
    }

    private class MyFragmentPagerAdapter extends FragmentPagerAdapter {

        public MyFragmentPagerAdapter(FragmentManager fm) {
            super(fm);
        }

        @Override
        public Fragment getItem(int position) {
            return PageFragment.newInstance(position, global.getMediaList().get(position).getFilePath());
        }

        @Override
        public int getCount() {
            return PAGE_COUNT;
        }
    }
}
